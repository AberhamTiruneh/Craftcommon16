package com.day7.encapsulation;

public class EncapsulationDemo1 {

	//let us insert employee info
	private String firstName = "mesued";
	private String lastName = "adem";
	private int age = 39;
	private double salary = 4500;
	
	public String getFirstName() {
		return firstName;
	}
	public String getLastName() {
		return lastName;
	}
	public int getAge() {
		return age;
	}
	public double getSalary() {
		return salary;
}
}
