package com.craft;

public class Example3 {

	public static void main(String[] args) {
        // byte type
        byte bortolla  = 20;
        System.out.println("b= " + bortolla);
    
        // short type
        short soboka = 20;
        System.out.println("s= " + soboka);
    
        // int type
        int Ersemu = 20;
        System.out.println("i= " + Ersemu);
    
        // long type
        long Kitilla = 20;
        System.out.println("l= " + Kitilla);
    
      }
    }      
